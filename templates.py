def get_template_by_name(template_name):
    """Get a specific template by name"""
    return AUTISM_STORYBOARD_TEMPLATES.get(template_name)


def list_all_templates():
    """List all available templates"""
    templates = []
    for key, template in AUTISM_STORYBOARD_TEMPLATES.items():
        templates.append({
            "key": key,
            "title": template["title"],
            "description": template["description"],
            "frame_count": len(template["frames"])
        })
    return templates


def get_prompts_from_template(template_name):
    """Extract just the prompts from a template"""
    template = get_template_by_name(template_name)
    if template:
        return [frame["prompt"] for frame in template["frames"]]
    return []


def create_custom_template(title, description, frames):
    """Create a custom template"""
    return {
        "title": title,
        "description": description,
        "frames": frames
    }


